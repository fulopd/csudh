package csudh;

public class Domain {

	private String domain;
	private String ipcim;
	
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getIpcim() {
		return ipcim;
	}
	public void setIpcim(String ipcim) {
		this.ipcim = ipcim;
	}
	
	public Domain(String domain, String ipcim) {
		super();
		this.domain = domain;
		this.ipcim = ipcim;
	}
	
	
	
	
}
